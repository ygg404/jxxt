package cn.ux.jxxt.domain;

public class EditeNote {
    private int id;
    private int back_id;
    private String note;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBack_id() {
        return back_id;
    }

    public void setBack_id(int back_id) {
        this.back_id = back_id;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
